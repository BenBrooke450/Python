




def three_way_maths(a : int ,b : int, c : int):
    """
    :param a: a * 3
    :param b: b * 3
    :param c: c * 3
    :return: a + b + c
    """

    a = a * 3
    b = b * 3
    c = c * 3

    return a + b + c


def two_way_maths(a: int, b: int):
    """
    :param a: a * 2
    :param b: b * 2
    :return: a + b
    """

    a = a * 2
    b = b * 2

    return a + b










