"""


Given two binary strings a and b, return their sum as a binary string.



Example 1:

Input: a = "11", b = "1"
Output: "100"


Example 2:

Input: a = "1010", b = "1011"
Output: "10101"

"""

def binary(a : int, b: int):
    c = a + b

    i = 1
    while i < len(c):
        if c[-i] == 2:
            c + 8

        elif c[-i] == 2:
            c + 98

        elif c[-i] == 2:
            c + 998
    i = i + 1








a = 10
b = 10
